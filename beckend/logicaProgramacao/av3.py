# Tudo fica misturado num texto só
# Você precisa inventar um jeito de separar as informações (como colocar | entre elas), e isso pode dar erro.

# Tem que ler tudo e mexer manualmente
# Se quiser mudar uma informação, precisa abrir o arquivo todo, pegar tudo, mudar a parte certa e salvar tudo de novo. Isso é chato e pode dar problema.

# Pode dar confusão se uma informação tiver o separador
# Se uma palavra tiver o | dentro, o programa vai se perder e pensar que tem mais coisas do que realmente tem.

# Só guarda texto, não entende números ou datas
# Tudo vira texto, e isso pode complicar se você quiser fazer contas com os números ou comparar datas.

# Não dá pra buscar direto uma coisa específica
# Se quiser só um carro específico, tem que ler tudo e procurar na mão.